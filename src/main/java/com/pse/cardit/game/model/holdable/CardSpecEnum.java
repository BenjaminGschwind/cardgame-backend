package com.pse.cardit.game.model.holdable;

/**
 * The enum containing various specifications of card decks.
 */
public enum CardSpecEnum {
    /**
     * CardDeck consisting of 52 cards.
     */
    FIFTY_TWO_CARD,
    /**
     * CardDeck consisting of 32 cards.
     */
    THIRTY_TWO_PIQUET,

    /**
     * CardDeck consisting of two 32 cardDecks
     */
    THIRTY_TWO_TWICE,
    /**
     * Empty CardDeck.
     */
    EMPTY
}
